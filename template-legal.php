Technical safeguards are defined in 445 CFR Part 164 § 164.304:
Technical safeguards mean the technology and the policy and procedures for its use that protect electronic
protected health information and control access to it.
The Security Rule’s technical safeguards do not mandate a specific technology solution but rather employ the
adaptable requirement that an entity use any and as many security measures as are reasonable and appropriate.
These security measures are required to meet several standards, as described below. Telehealthcare meets -- and
in many cases exceeds -- these standards while bringing innovative flexibility and features to healthcare users.
